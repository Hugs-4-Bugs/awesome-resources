CREATE DATABASE flights
USE flights

CREATE TABLE USER
(ID INT NOT NULL AUTO_INCREMENT, FIRST_NAME VARCHAR (20), LAST_NAME VARCHAR (20),
EMAIL VARCHAR (125),
PASSWORD VARCHAR (20), PRIMARY KEY(ID),
UNIQUE KEY(EMAIL)
)

SELECT * FROM USER


CREATE TABLE FLIGHT
(ID INT NOT NULL AUTO_INCREMENT,
FLIGHT_NUMBER VARCHAR (20) NOT NULL,
OPERATING_AIRLINES VARCHAR (20) NOT NULL,
DEPARTURE_CITY VARCHAR (20) NOT NULL,
ARRIVAL_CITY VARCHAR (20) NOT NULL,
DATE_OF_DEPARTURE DATE NOT NULL,
ESTIMATED_DEPARTURE_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP, PRIMARY KEY(ID)
)

SELECT * FROM FLIGHT


CREATE TABLE PASSENGER
(ID INT NOT NULL AUTO_INCREMENT, FIRST_NAME VARCHAR (20), LAST_NAME VARCHAR (20), MIDDLE_NAME VARCHAR (20), EMAIL VARCHAR (125),
PHONE VARCHAR (10), PRIMARY KEY(ID)
)

SELECT * FROM PASSENGER


CREATE TABLE RESERVATION
(ID INT NOT NULL AUTO_INCREMENT, CHECHED_IN TINYINT (1), NUMBER_OF_BAGS INT, PASSENGER_ID INT,
FLIGHT_ID INT,
CREATED TIMESTAMP DEFAULT CURRENT_TIMESTAMP, PRIMARY KEY(ID),FOREIGN KEY(PASSENGER_ID) REFERENCES PASSENGER(ID) ON DELETE CASCADE, FOREIGN KEY(FLIGHT_ID) REFERENCES FLIGHT(ID)
)

SELECT * FROM RESERVATION


